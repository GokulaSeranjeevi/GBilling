package com.skywings.gbilling.model;

public class Operator {

	private int operatorId;
	private String operNameS;
	private String operPass;
	private String Active;

	public int getOperatorId() {
		return operatorId;
	}

	public void setOperatorId(int string) {
		this.operatorId = string;
	}

	public String getOperNameS() {
		return operNameS;
	}

	public void setOperNameS(String operNameS) {
		this.operNameS = operNameS;
	}

	public String getOperPass() {
		return operPass;
	}

	public void setOperPass(String operPass) {
		this.operPass = operPass;
	}

	public String getActive() {
		return Active;
	}

	public void setActive(String active) {
		Active = active;
	}

}
